using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using QLDT.Common.DAL;
using QLDT.Common.Res;
using QLDT_DAL.Models;

namespace QLDT_DAL
{
    public class ResultRep : GenericRep<Test6Context, Result>
    {
        public ResultRep()
        {

        }

        #region -- Overrides --

        public override Result Read(int id)
        {
            var res = All.FirstOrDefault(p => p.ResultId == id);
            return res;
        }

        #endregion

        #region -- Methods --

        public SingleRes CreateResult(Result result)
        {
            var res = new SingleRes();
            using (var context = new Test6Context())
            {
                using (var tran = context.Database.BeginTransaction())
                {
                    try
                    {
                        context.Results.Add(result);
                        context.SaveChanges();
                        tran.Commit();
                    }
                    catch (Exception ex)
                    {
                        tran.Rollback();
                        res.SetError(ex.StackTrace);
                    }
                }
            }
            return res;
        }

        public SingleRes UpdateResult(Result result)
        {
            var res = new SingleRes();
            using (var context = new Test6Context())
            {
                using (var tran = context.Database.BeginTransaction())
                {
                    try
                    {
                        context.Results.Update(result);
                        context.SaveChanges();
                        tran.Commit();
                    }
                    catch (Exception ex)
                    {
                        tran.Rollback();
                        res.SetError(ex.StackTrace);
                    }
                }
            }
            return res;
        }

        public SingleRes DeleteResult(int id)
        {
            var res = new SingleRes();
            using (var context = new Test6Context())
            {
                using (var tran = context.Database.BeginTransaction())
                {
                    try
                    {
                        var resultToDelete = context.Results.Find(id);
                        if (resultToDelete != null)
                        {
                            context.Results.Remove(resultToDelete);
                            context.SaveChanges();
                            tran.Commit();
                            res.SetData("200", "Result deleted successfully.");
                        }
                        else
                        {
                            res.SetError("EZ404", "Result not found for deletion.");
                            tran.Rollback();
                        }
                    }
                    catch (Exception ex)
                    {
                        tran.Rollback();
                        res.SetError(ex.StackTrace);
                    }
                }
            }
            return res;
        }

        #endregion
    }
}